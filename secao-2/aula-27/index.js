//Objeto Math
let num1 = 9.54578;
console.log(Math.floor(num1)); // Arredonda para baixo
console.log(Math.ceil(num1)); // Arredonda para cima
console.log(Math.round(num1)); // Arredonda para o mais próximo
console.log(Math.max(1,2,2,3,4,5,6,77,765)); // Maior número da sequencia
console.log(Math.min(1,2,2,3,4,5,6,77,765)); // Menor número da sequencia
console.log(Math.random()); // Gera número aleatório
console.log(Math.random() * (10 - 5) + 5); // Gera número aleatório
console.log(Math.PI); //PI
console.log(Math.pow(2, 10)); // Potenciação
console.log(Math.sqrt(num1)); // Raiz quadrada






